public class nonnumericcasting {

    public static void main(String[] args) {
        char char1 = 65;
        System.out.println(char1);
        byte byte1 = 'A';
        System.out.println(byte1);
        char char2 = 8253;
        System.out.println(char2);
        int int1 = '?';
        System.out.println(int1);
        byte byte2 = '?';
        System.out.println(byte2);
    }
}
