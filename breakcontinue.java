public class breakcontinue {
    public static void main(String[] args) {
        // break and continue using loop
        for(int i = 0; i< 67; i++){
            System.out.println(i);
            System.out.println("Java is great!");
            if (i ==2) {
               System.out.println("End the loop."); 
               break;
            }
        }
    }
}
